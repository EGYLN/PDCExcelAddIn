namespace BBS.ST.BHC.BSP.PDC.Lib.Exceptions
{
  /// <summary>
  /// Defines the message codes for all PDCLibFaults in this assembly
  /// </summary>
  public enum PDCFaultMessage
  {
    //Login failure messages:
    /// <summary>
    /// Invalid combination of userid and password
    /// </summary>
    LOGIN_INVALID_CREDENTIALS,
    /// <summary>
    /// The password has expired, user should probably ask the help desk
    /// </summary>
    LOGIN_PASSWORD_EXPIRED,
    /// <summary>
    /// The account is disabled, user should probably ask the help desk
    /// </summary>
    LOGIN_ACCOUNT_DISABLED,
    /// <summary>
    /// The account has expired, user should probably ask the help desk
    /// </summary>
    LOGIN_ACCOUNT_EXPIRED,
    /// <summary>
    /// The user has to change his/her password.
    /// </summary>
    LOGIN_MUST_CHANGE_PASSWORD,
    /// <summary>
    /// Login is temporarely not possible
    /// </summary>
    LOGIN_CURRENTLY_NOT_PERMITTED,
    /// <summary>
    /// Unknown failure
    /// </summary>
    LOGIN_UNKNOWN_FAULT,
    /// <summary>
    /// User is not member of the PDC User group
    /// </summary>
    LOGIN_NO_PDC_USER,
    /// <summary>
    /// There was no user entry in the directory
    /// </summary>
    LOGIN_NO_USER_ENTRY_FOUND,
    /// <summary>
    /// General failure message when autorization in PHUser failed
    /// </summary>
    LOGIN_FAILED,

    /// <summary>
    /// Too many search parameters specified.
    /// </summary>
    TOO_MANY_SEARCH_PARAMETERS,
    /// <summary>
    /// General failure message when a service call failed
    /// </summary>
    SERVER_FAILURE

  }
}
